package com.bridgelabz.linecomparisionoop.dto;

import java.util.Scanner;

/**
 * Scanner class
 * 
 * @author Arundathi
 *
 */
public class InputScanner {
	static final Scanner SC = new Scanner(System.in);

	/**
	 * In this method we are taking integer type input
	 * 
	 * @return
	 */
	public static int inputInt() {
		return SC.nextInt();
	}
}
